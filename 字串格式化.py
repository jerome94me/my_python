#使用f-string的字串格式化

#創三個變量
NT1 = 3.321
NT2 = -77
NT3 = 15.11
#印出
print(f"價格1為 {NT1}\n") 
print(f"價格2為 {NT2}\n")
print(f"價格3為 {NT3}\n")
#--------------------------------------小數點格式相同-----------------------------

